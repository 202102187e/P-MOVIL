void main(List<String> args) {
  // Estructuras Repetitivas ,permiten iterar sobre una coleccion de elementos
  // for, foreach ,while, do while


  for (var i = 0; i < 5; i++) {
    print("i vale $i");
  }

  int i =0;
  while (i<5) {
    print("(while)  es $i");
    i++;
  }

  int c =0 ;

  do {
    print("(do while) es $c");
    c++;
  } while (c<5);






}